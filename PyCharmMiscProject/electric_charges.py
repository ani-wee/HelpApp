hours = int(input("Enter the KW hours used:"))
#test case number 1
first_rate = 7.633 / 100
second_rate = 9.259 / 100
#rate in dollars
if hours <=1000:
    amount = (1000 * first_rate)
else:
    amount = (1000 * first_rate) + ((hours - 1000) * second_rate)
print("Amount owed is $", amount, sep="")
